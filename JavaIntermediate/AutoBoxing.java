package JavaIntermediate;

public class AutoBoxing {
    //Auto-boxing: Automatically converting primitive to its wrapperClass.

    Integer num = 10;   //New Integer(10), int is automatically boxed to an integer type(reference)

    //Unboxing: Converting a Wrapper back to a primitive

    int value = num;    // Integer is automatically unboxed to an int type(primitive)

}
